    private void btnCekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekActionPerformed
    try {
        // Ambil input dari text field
        String input = txtAngka.getText().trim();

        // Validasi: tidak boleh kosong
        if (input.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                    "Input tidak boleh kosong!", 
                    "Peringatan", 
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Cek apakah input numerik
        int angka = Integer.parseInt(input);

        // Tentukan genap atau ganjil
        String hasil;
        if (angka % 2 == 0) {
            hasil = angka + " adalah bilangan GENAP.";
        } else {
            hasil = angka + " adalah bilangan GANJIL.";
        }

        // Tambahkan variasi: cek bilangan prima
        boolean prima = true;
        if (angka <= 1) {
            prima = false;
        } else {
            for (int i = 2; i <= Math.sqrt(angka); i++) {
                if (angka % i == 0) {
                    prima = false;
                    break;
                }
            }
        }

        if (prima) hasil += " Juga merupakan BILANGAN PRIMA.";
        else hasil += " Bukan bilangan prima.";

        // Tampilkan hasil
        txtHasil.setText(hasil);
        JOptionPane.showMessageDialog(this, hasil, 
                "Hasil Cek", JOptionPane.INFORMATION_MESSAGE);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, 
                "Masukkan hanya angka yang valid!", 
                "Error", JOptionPane.ERROR_MESSAGE);
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnCekActionPerformed

